package Day10_Tasks;

public class Task03_BiggerNumber {
	
	public static void main(String[] args) {
		
	int n1 = 15;
	int n2 = 45;
	int n3 = 35;
	
	
	if (n1 > n2 && n1 > n3)
		System.out.println("n1 is bigger");
		
	else if (n2 > n1 && n2 > n3)
		System.out.println("n2 is bigger");
		
	else if (n3 > n1 && n3 > n2)
		System.out.println("n3 is bigger");
	
		
	}
}
