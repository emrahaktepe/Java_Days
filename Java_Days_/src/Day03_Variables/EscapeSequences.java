package Day03_Variables;

public class EscapeSequences {

	/*
	 \n or \r starts from the new line
	 \t horizontal tab
	 \\ backslash
	 \' prints single quote
	 \" prints double quote
	 
	 */
	
	public static void main(String[] args) {
		System.out.println("Wassup Yall Cybertek\n Bathch 12 students");
		
		System.out.println("=============================");
		
		System.out.print("Tomorrow is off\n");
		System.out.print("True\n");
		System.out.print("False\n");
		
		System.out.println("=============================");

		System.out.println("\tThe class starts tomorrow");
	
		System.out.println("=============================");

		System.out.println("\\Erzincan");
		System.out.println("\'Deneme\'");
		System.out.println("The name of it is \"Menemen\"");
		
		System.out.println("=============================");

		
	}
	
}
