package Day03_Variables;

public class println_vs_print {

	public static void main(String[] args) {
		System.out.println("Wassup Yall"); // prints from a new line
		System.out.println("how you doin?");
		
		System.out.println("===================");
		
		System.out.print("Wassup Yall"); 
		System.out.print("how you doin?");
		System.out.println();
		
		System.out.println("===================");

		System.out.println("Today is");
		System.out.print("Tuesday, August 14");
	}
}
